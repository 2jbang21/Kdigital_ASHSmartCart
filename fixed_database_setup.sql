SELECT '=== 상품별 구매 빈도 ===' as info;
SELECT p.product_name, COUNT(*) as purchase_count
FROM Purchase pu
JOIN Product p ON pu.product_id = p.product_id
GROUP BY pu.product_id, p.product_name
ORDER BY purchase_count DESC;